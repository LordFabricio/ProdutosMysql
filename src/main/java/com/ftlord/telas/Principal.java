    /**
     * Metodo que Ordena a Pesquisa de UM unico Produto
     */
    private void pesqP() {
        try {
            int id = (int) this.tabela.getValueAt(0, 0);
            p = controleP.pegaPC(id);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Produto Não Encontrado", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }

    }

    /**
     * Metodo que Ordena Salvar o Produto no Banco de Dados
     */
    private void salvarP() {
        p.setDescProduto(this.descC.getText());
        p.setQuantProduto(Double.parseDouble(this.quantC.getText()));
        p.setValorProduto(Double.parseDouble(this.valorC.getText()));
        if (controleP.salvarPC(p) > 0) {
            this.carregaP();
            limpaAreaCad();
            edicaoAreaCad(false);
            imagem.setIcon(null);
            JOptionPane.showMessageDialog(this, "Cadastro Realizado com Sucesso!!!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro... ao Cadastrar", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }

    /**
     * Metodo que Ordena Alterar o Produto no Banco de Dados
     */
    private void alterarP() {
        p.setDescProduto(this.descC.getText());
        p.setQuantProduto(Double.parseDouble(this.quantC.getText()));
        p.setValorProduto(Double.parseDouble(this.valorC.getText()));
        if (controleP.alterarPC(p)) {
            this.carregaP();
            limpaAreaCad();
            edicaoAreaCad(false);
            imagem.setIcon(null);
            JOptionPane.showMessageDialog(this, "Alteração Realizada com Sucesso!!!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro... ao Alterar", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }

    /**
     * Metodo que Ordena Carreagar em uma LISTA os Produtos do Banco de Dados
     */
    private void carregaP() {
        listaProdutos = controleP.listaPC();
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        int cont = listaProdutos.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaProdutos.get(i).getIdProduto(),
                listaProdutos.get(i).getDescProduto(),
                listaProdutos.get(i).getQuantProduto(),
                listaProdutos.get(i).getValorProduto()
            });
        }
    }

