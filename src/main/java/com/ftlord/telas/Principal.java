    private void salvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvaActionPerformed
        if (salvarAlterar.equals("salvar")) {
            this.salvarP();
            salvarAlterar = "limpo";
        } else if (salvarAlterar.equals("alterar")) {
            this.alterarP();
            salvarAlterar = "limpo";
        } else if (salvarAlterar.equals("limpo")) {
            JOptionPane.showMessageDialog(this, "Adicione ou Altere um Produto Antes", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_salvaActionPerformed

    private void excluiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluiActionPerformed
        linha = this.tabela.getSelectedRow();
        if (linha >= 0) {
            int id = (int) this.tabela.getValueAt(linha, 0);
            if (controleP.escluirPC(id)) {
                this.carregaP();
                limpaAreaCad();
                edicaoAreaCad(false);
                JOptionPane.showMessageDialog(this, "Produto Excluido com Sucesso!!!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro... ao Excluir", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Produto", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_excluiActionPerformed

