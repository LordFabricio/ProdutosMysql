    private void alteraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alteraActionPerformed
        String file = descC.getText();
        linha = this.tabela.getSelectedRow();
        if (linha >= 0) {
            salvarAlterar = "alterar";
            edicaoAreaCad(true);
            novo.setEnabled(false);
            exclui.setEnabled(false);
            pesquisa.setEnabled(false);
            altera.setEnabled(false);
            codigoC.setEnabled(false);
            salva.setEnabled(true);
            cancela.setEnabled(true);
            this.descC.setText(p.getDescProduto());
            this.quantC.setText(String.valueOf(p.getQuantProduto()));
            this.valorC.setText(String.valueOf(p.getValorProduto()));
            this.descC.selectAll();
            this.descC.requestFocus();
            try {
                int id = (int) this.tabela.getValueAt(linha, 0);
                p = controleP.pegaPC(id);
                this.descC.setText(p.getDescProduto());
                this.quantC.setText(String.valueOf(p.getQuantProduto()));
                this.valorC.setText(String.valueOf(p.getValorProduto()));
                imagem.setIcon(a.pegaImagem(file = descC.getText()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Selecione Um Registro", "Aviso", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Produto", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_alteraActionPerformed

    private void pesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesquisaActionPerformed
        if (!"CODIGO".equals(codigoC.getText())) {
            DefaultTableModel tb = (DefaultTableModel) this.tabela.getModel();
            final TableRowSorter<TableModel> classificador = new TableRowSorter<>(tb);
            this.tabela.setRowSorter(classificador);
            String id = this.codigoC.getText();
            classificador.setRowFilter(RowFilter.regexFilter(id, 0));
            this.pesqP();
        } else {
            JOptionPane.showMessageDialog(this, "Digite o Codigo do Produto", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
        tudo.setEnabled(true);
        codigoC.setText("CODIGO");
    }//GEN-LAST:event_pesquisaActionPerformed

